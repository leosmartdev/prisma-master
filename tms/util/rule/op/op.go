// Package op provides constants to determine string compare operations.
package op

// Operators
const (
	Eq    = "$eq"    // Equal
	Gt    = "$gt"    // Greater Than
	Gte   = "$gte"   // Greater Than or Equal
	Lt    = "$lt"    // Less Than
	Lte   = "$lte"   // Less Than or Equal
	And   = "$and"   // Logical And
	Or    = "$or"    // Logical Or
	RegEx = "$regex" // Regular expression
)
